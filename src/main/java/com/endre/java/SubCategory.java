package com.endre.java;

public class SubCategory {

    private int id;
    private String name;

    public SubCategory() {
    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
